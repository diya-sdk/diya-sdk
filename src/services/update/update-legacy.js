Update.prototype.listPackages = function(callback){

	this.node.get({
		service: 'update',
		func: 'ListPackages'
	}, function(data){
		if(data.packages) 
			callback(null,data.packages); 
		if(data.error)
			callback(data.error,null);
		
	}); 
}
	
Update.prototype.updateAll = function(callback){

	this.node.get({
		service: 'update',
		func: 'UpdateAll'
	}, function(data){
		if(data.packages) 
			callback(null,data.packages); 
		if(data.error)
			callback(data.error,null);
	}); 
}
	
Update.prototype.installPackage = function(pkg, callback){
		
	if ((pkg === 'Undefined') || (typeof pkg !== 'string') || (pkg.length < 2)){
		callback('undefinedPackage',null);
	}
	else {
	
		var INVALID_PARAMETERS_REGEX = /^-|[^\s]\s+[^\s]|-$/;
		var testNamePkg= INVALID_PARAMETERS_REGEX.test(pkg);
		if (testNamePkg)
			callback("InvalidParameters",null);
		else{
				
			this.node.get({
				service: 'update',
				func: 'InstallPackage',
				data:{
					package: pkg,
				}
			}, function(data){
				if(data.packages) 
					callback(null,data.packages); 
				if(data.error)
					callback(data.error,null);
					
				
			});
		}
	}
	
}

Update.prototype.removePackage = function(pkg, callback){
		
	if ((pkg === 'Undefined') || (typeof pkg !== 'string') || (pkg.length < 2)){
		callback('undefinedPackage',null);
	}
	else {
	
		var INVALID_PARAMETERS_REGEX = /^[+ -]|[^\s]\s+[^\s]|[+ -]$/;
		var testNamePkg= INVALID_PARAMETERS_REGEX.test(pkg);
		if (testNamePkg)
			callback("InvalidParameters",null);
		else{
				
			this.node.get({
				service: 'update',
				func: 'RemovePackage',
				data:{
					package: pkg,
				}
			}, function(data){
				if(data.packages) 
					callback(null,data.packages); 
				if(data.error)
					callback(data.error,null);
					
				
			});
		}
	}
	
}
		
		
